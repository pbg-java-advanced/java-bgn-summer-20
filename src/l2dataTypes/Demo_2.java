package l2dataTypes;

/**
 * Illustrates various operations with double
 */
public class Demo_2 {
    public static void main(String[] args) {

        System.out.print("5.0 + 2.0 = ");
        System.out.println(5.0 + 2.0);
        System.out.println();
        System.out.print("5 - 2.0 = ");
        System.out.println(5 - 2.0);
        System.out.println();
        System.out.print("5.0 / 2 = ");  // division is exact!
        System.out.println(5.0 / 2);
        System.out.println();
        System.out.print("5.5 % 2 = ");
        System.out.println(5.5 % 2);
        System.out.println();
    }
}
