package l3conditionals;

/**
 * Prints several characters, together
 * with their ASCII values
 */
public class Demo_1 {
    public static void main(String[] args) {
        char A = 'A';   // 65
        System.out.println(A + ": " + (int)A);
        char Z = 'Z';
        System.out.println(Z + ": " + (int)Z);
        char a = 'a';
        System.out.println(a + ": " + (int)a);
        char z = 'z';
        System.out.println(z + ": " + (int)z);
        char x = 10050;
        System.out.println(x + ": " + (int)x);
    }
}

