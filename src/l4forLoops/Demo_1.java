package l4forLoops;

/**
 * Use a for loop to print the same line several times.
 */
public class Demo_1 {
    public static void main(String[] args) {
        System.out.println("Here is my mantra:");
        for (int i = 1; i <= 10; i++) {
            System.out.println("I am such a good coder!");
        }
        System.out.println("I am simply the best!");
    }
}
